export function getGlyph(ch: string): string[] {
  const g = G[ch.toUpperCase()] ?? G[" "];
  return g;
}

/** Width in dots for one character including trailing gap (except caller can skip gap after last char). */
export function glyphSlotWidth(): number {
  return GLYPH_W + GLYPH_GAP;
}

/** Pixel width of string in dot columns (gaps between letters, no gap after last char). */
export function measureTextWidth(text: string): number {
  if (text.length === 0) return 0;
  return text.length * GLYPH_W + Math.max(0, text.length - 1) * GLYPH_GAP;
}
